package Day_17_Loops;
    public class MultiplicationTable {
        public static void main(String[] args) {

            int num = 7;

            for (int i = 1; i <= 10; i++) {
                System.out.println(num + " * " + i + " = " + (i * num) );
            }
        }
    }
            // 1 * 1 = 1
            // 1 * 2 = 2

