package Day_07_Relational_Operators;

public class Operator_Practice {
    public static void main(String[] args) {

        int main = 10;
        main++; // post-increment; | no other code, just increment by 1. Here main = 10;
        System.out.println(main); // main = 11.

        main--; // post-decrement; main = 11;
        System.out.println(main); // main = 10;
        System.out.println(main++); // main = 10;
        System.out.println(++main); // main = 12


    }
}
