package Day_04_Tasks_int_byte_double_float;

public class byte_and_int {
    public static void main (String [] args) {

        int num = 5; // We have a variable called num which had int data type and number 5 is assigned to it.
        // We have declared variable called num which had int data type and number 5 is assigned to it.

        System.out.println("5 + " + num + " = 10");

        byte age = 90; // I declared a variable called age which has int data type and number 50 is assigned to it.

        System.out.println("50");  // Hardcoded -- mean you have to change it manually.
        System.out.println("byte"); // here "age" is not a variable. It is just a word that we are printing.
        System.out.println(age); // here "age" is a variable, because we don't put "".

        age = 15;

        System.out.println(age); // here we write "age = 15" and after this age will change to 15, because Java remembers the last value.


    }
}


