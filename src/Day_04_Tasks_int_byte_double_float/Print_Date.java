package Day_04_Tasks_int_byte_double_float;

public class Print_Date {
    public static void main (String [] args){

        int month = 9;
        int day = 7;
        int year = 2023;

        System.out.println(month + "/" + day + "/" + year);
        System.out.println(month + "_" + day + "_" + year);
        System.out.println(month + "-" + day + "-" + year);
        System.out.println(month + "|" + day + "|" + year);
        System.out.println(month + "<>" + day + "<>" + year);

        System.out.println();

    }
}
