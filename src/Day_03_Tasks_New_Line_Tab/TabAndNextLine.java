package Day_03_Tasks_New_Line_Tab;

public class TabAndNextLine {
    public static void main (String [] args) {

        System.out.println("This is not tabbed");
        System.out.println("\tThis is not tabbed 1 time");  // this is tabbed 1 time
        System.out.println("    This is not tabbed");       // 4 space is equal 1 tab
        System.out.println("\t\tThis is not tabbed 2 times");
        System.out.println("\t\t\t\t\t\t\t\t\t\tThis is not tabbed");

        // The followings are about \n - new line
        System.out.println("1) Go to store");
        System.out.println("2) Grab milk");
        System.out.println("3) Pay cashier");


        System.out.println("1) Go to the store\n2) Grab milk\n3) Pay cashier");



    }
}
